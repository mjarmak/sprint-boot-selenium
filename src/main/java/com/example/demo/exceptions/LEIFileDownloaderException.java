package com.example.demo.exceptions;

public class LEIFileDownloaderException extends Exception {

	public LEIFileDownloaderException() {
		super();

	}

	public LEIFileDownloaderException(String message, Throwable cause, boolean enableSuppression,
                                      boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public LEIFileDownloaderException(String message, Throwable cause) {
		super(message, cause);

	}

	public LEIFileDownloaderException(String message) {
		super(message);

	}

	public LEIFileDownloaderException(Throwable cause) {
		super(cause);

	}

}
